#!/usr/bin/env python3
"""
st-merge — Synthesize multiple AI stories into one

Two modes are selected automatically based on available data:

  simple    All selected stories are passed to the synthesizer AI which
            produces a single cohesive report. No fact-check data required.
            This is the fallback when no fact-check data exists.

  quality   Uses available fact-check data to guide the synthesis. The
            base story is the highest-scoring story. The AI that authored
            the base story performs the rewrite — it already knows the
            voice, structure and topic. Other stories contribute verified
            facts to correct weak claims, not prose to be stitched in.
            The result is a single consistent voice throughout.

The mode is chosen automatically. Use --simple or --quality to override.

In quality mode the synthesizer AI is always the author of the base story.
Use --ai only to override the synthesizer in simple mode, or use --base to
change which story (and therefore which AI) anchors the quality rewrite.

Usage:
  st-merge file.json                        # auto mode, original stories only
  st-merge --stories 1 2 3 file.json        # merge a subset of stories
  st-merge --min-score 1.3 file.json        # only merge stories scoring >= 1.3
  st-merge --all file.json                  # merge all stories including derived
  st-merge --simple file.json               # force simple mode
  st-merge --quality file.json              # force quality mode (fails if no data)
  st-merge --ai anthropic file.json         # synthesizer AI for simple mode only
  st-merge --base 3 file.json               # use story 3 as base (its AI rewrites)

Output:
  Appends a new story to the .json container. The merged story records which
  source stories and AI contributed, and which mode was used.

See README_merge.md for the full design notes and roadmap.
"""

